from openerp import models, fields, api
from openerp.exceptions import ValidationError


class EmsLocation(models.Model):


    def name_get(self, cr, uid, ids, context=None):
        if context is None:
            context = {}
        if not ids:
            return []
        reads = self.read(cr, uid, ids, ['name','parent_id'], context=context)
        res = []
        for record in reads:
            name = record['name']
            if record['parent_id']:
                name = record['parent_id'][1]+' / '+name
            res.append((record['id'], name))
        return res

    def _location_name_get_fnc(self, cr, uid, ids, prop, unknow_none, context=None):
        res = self.name_get(cr, uid, ids, context=context)
        return dict(res)


    _name = 'ems.location'
    _order="name asc"

    name = fields.Char(string='Name', required=True)
    complete_name = fields.Char(string='Name', store=True, compute='_location_name_get_fnc')
    parent_id = fields.Many2one('ems.location', string='Belongs To', required=True)
    latitude = fields.Float(string='Latitude')
    longitude = fields.Float(string='Longitude')
    level = fields.Selection([('1','Country'),
				('2','Island'),
				('3','County'),
				('4','Parish')
		], string='Place of birth', required=True)



